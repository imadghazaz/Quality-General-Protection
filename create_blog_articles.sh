                                    QGP propose des contrats de maintenance adaptés à vos besoins :
                                </p>
                                <ul style="list-style-type: disc; margin-left: 30px; line-height: 2; margin-bottom: 20px;">
                                    <li>Contrôles trimestriels programmés</li>
                                    <li>Vérification annuelle complète</li>
