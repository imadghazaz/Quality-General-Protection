                                <h2 style="margin-bottom: 20px;">L'Importance de la Maintenance des Extincteurs</h2>

                                <p style="line-height: 1.8; margin-bottom: 20px;">
                                    Les extincteurs sont la première ligne de défense contre les incendies. Cependant, un extincteur mal
                                    entretenu peut s'avérer inefficace, voire dangereux en situation d'urgence. La maintenance régulière
                                    est donc absolument essentielle.
                                </p>

                                <h3 style="margin-top: 30px; margin-bottom: 15px;">Pourquoi la Maintenance est Cruciale</h3>

                                <h4 style="margin-top: 25px; margin-bottom: 10px;">Garantir l'Efficacité</h4>
                                <p style="line-height: 1.8; margin-bottom: 15px;">
                                    Un extincteur qui n'a pas été vérifié depuis longtemps peut perdre sa pression, voir son agent
                                    extincteur se dégrader ou présenter des défauts mécaniques. Une maintenance régulière assure que
                                    l'extincteur fonctionnera parfaitement le jour où vous en aurez besoin.
                                </p>

                                <h4 style="margin-top: 25px; margin-bottom: 10px;">Respecter la Législation</h4>
                                <p style="line-height: 1.8; margin-bottom: 15px;">
                                    La réglementation marocaine impose des contrôles réguliers des extincteurs. Le non-respect de ces
                                    obligations peut entraîner des sanctions et compromettre vos assurances en cas de sinistre.
                                </p>

                                <h4 style="margin-top: 25px; margin-bottom: 10px;">Protéger Vos Équipes</h4>
                                <p style="line-height: 1.8; margin-bottom: 15px;">
                                    Un extincteur défectueux peut mettre en danger la personne qui tente de l'utiliser. La maintenance
                                    prévient les risques de dysfonctionnement ou d'explosion.
                                </p>

                                <h3 style="margin-top: 30px; margin-bottom: 15px;">Les Différents Types de Contrôles</h3>

                                <h4 style="margin-top: 25px; margin-bottom: 10px;">Vérification Trimestrielle</h4>
                                <p style="line-height: 1.8; margin-bottom: 15px;">
                                    Tous les 3 mois, un contrôle visuel doit être effectué :
                                </p>
                                <ul style="list-style-type: disc; margin-left: 30px; line-height: 2; margin-bottom: 20px;">
                                    <li>Vérification de l'accessibilité et de la signalisation</li>
                                    <li>Contrôle de la pression (manomètre dans le vert)</li>
                                    <li>État général de l'extincteur (pas de chocs, corrosion)</li>
                                    <li>Présence et lisibilité des étiquettes</li>
                                    <li>Scellement en place et intact</li>
                                </ul>

                                <h4 style="margin-top: 25px; margin-bottom: 10px;">Vérification Annuelle</h4>
                                <p style="line-height: 1.8; margin-bottom: 15px;">
                                    Une fois par an, un contrôle approfondi est nécessaire :
                                </p>
                                <ul style="list-style-type: disc; margin-left: 30px; line-height: 2; margin-bottom: 20px;">
                                    <li>Démontage et inspection complète</li>
                                    <li>Vérification de la qualité de l'agent extincteur</li>
                                    <li>Contrôle de tous les composants mécaniques</li>
                                    <li>Test de la valve et de la gâchette</li>
                                    <li>Pesée de l'extincteur</li>
                                    <li>Remise en pression si nécessaire</li>
                                </ul>

                                <h4 style="margin-top: 25px; margin-bottom: 10px;">Révision Décennale</h4>
                                <p style="line-height: 1.8; margin-bottom: 15px;">
                                    Tous les 10 ans, une révision complète avec épreuve hydraulique est obligatoire pour vérifier
                                    la résistance de la cuve.
                                </p>

                                <h3 style="margin-top: 30px; margin-bottom: 15px;">Signes qu'un Extincteur Doit Être Remplacé</h3>
                                <ul style="list-style-type: disc; margin-left: 30px; line-height: 2; margin-bottom: 20px;">
                                    <li>Corrosion importante ou perforation de la cuve</li>
                                    <li>Manomètre défectueux ou illisible</li>
                                    <li>Gâchette ou flexible endommagé</li>
                                    <li>Absence de pression persistante après recharge</li>
                                    <li>Extincteur ayant plus de 20 ans</li>
                                </ul>

                                <h3 style="margin-top: 30px; margin-bottom: 15px;">Les Services QGP</h3>
